package com.mycompany.mavenproject1;

/**
 *
 * @author yidingweng
 */
public class Snacks {
    private int index;
    private String name;
    private int amount;
    private double price;
    
    public int getIndex(){
        return index;
    }
    
    public void setIndex(int indexN){
        index = indexN;
    }
    
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }
    
    
}
